/**
 * Helper to build a short polyline from two anchor points
 * (start + end of street segment).
 */
function seg(
  start: [number, number],
  end: [number, number]
): { center: { lat: number; lng: number }; polyline: [number, number][] } {
  const mid: [number, number] = [(start[0] + end[0]) / 2, (start[1] + end[1]) / 2];
  return {
    center: { lat: mid[0], lng: mid[1] },
    polyline: [start, mid, end],
  };
}

